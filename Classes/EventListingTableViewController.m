#pragma mark -
#pragma mark Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    NSUInteger index[2] = { 0, selectedIndex };
    
    if ([tableView cellForRowAtIndexPath:[NSIndexPath indexPathWithIndexes:index length:2]].accessoryType == UITableViewCellAccessoryCheckmark) {
        // unselect old selection
        [tableView cellForRowAtIndexPath:[NSIndexPath indexPathWithIndexes:index length:2]].accessoryType = UITableViewCellAccessoryNone;
    }
    
    [tableView cellForRowAtIndexPath:indexPath].accessoryType = UITableViewCellAccessoryCheckmark;
    [tableView cellForRowAtIndexPath:indexPath].selected = NO;
	NSLog(@"num of orders %d", [orders count]);
    selectedIndex = [indexPath row];
	
	TicketDetailViewController *controller = [[TicketDetailViewController alloc] initWithNibName:@"TicketDetailViewController" bundle:nil];
//	controller.barcode = @"2338872730220599001";
	controller.barcode = @"000";
	Order* order = [orders objectAtIndex:[indexPath row]];
	controller.order = order;
	controller.attendee = state.currentAttendee;
	[parentVC presentFinishedButton];

	[self presentModalViewController:controller animated:YES];
	
	[controller release];
}


